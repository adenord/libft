#include "libft.h"

/* ********************************** */
/*                                    */
/*  calculate the length of a string  */
/*                                    */
/* ********************************** */

size_t	ft_strlen(const char *s)
{
	unsigned int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}
