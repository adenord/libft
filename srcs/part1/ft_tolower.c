#include "libft.h"

/* ****************************** */
/*                                */
/*  convert letter to lower case  */
/*                                */
/* ****************************** */

int	ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
		return ((c - 65) + 97);
	else
		return (c);
}
