#include <stdlib.h>

static void	ft_bzero(void *s, size_t n)
{
	while (n > 0)
	{
		if (*(char *)s != 0)
			*(char *)s = 0;
		s += 1;
		n--;
	}
}

void	*ft_calloc(size_t count, size_t size)
{
	void	*ret;

	ret = malloc(count * size);
	if (!ret)
		return (NULL);
	ft_bzero(ret, count * size);
	return (ret);
}
