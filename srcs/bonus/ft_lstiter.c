#include "libft.h"

/* ********************************************** */
/*                                                */
/*  applies a function to each element of a list  */
/*                                                */
/* ********************************************** */

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
