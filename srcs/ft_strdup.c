#include <stdlib.h>

static int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s1)
{
	int		src_len;
	char	*dest;

	src_len = ft_strlen(s1);
	dest = (char *)malloc(sizeof(char) * src_len + 1);
	if (!dest)
		return (0);
	ft_strcpy(dest, s1);
	return (dest);
}
